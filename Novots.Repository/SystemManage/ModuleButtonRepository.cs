using Conan.Data;
using Conan.Domain.Entity.SystemManage;
using Conan.Domain.IRepository.SystemManage;
using Conan.Repository.SystemManage;
using System.Collections.Generic;

namespace Conan.Repository.SystemManage
{
    public class ModuleButtonRepository : RepositoryBase<ModuleButtonEntity>, IModuleButtonRepository
    {
        public void SubmitCloneButton(List<ModuleButtonEntity> entitys)
        {
            using (var db = new RepositoryBase().BeginTrans())
            {
                foreach (var item in entitys)
                {
                    db.Insert(item);
                }
                db.Commit();
            }
        }
    }
}
